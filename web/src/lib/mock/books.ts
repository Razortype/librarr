import type { BookListItem } from "@/lib/types";

export interface MockBookDetail {
  subtitle?: string | null;
  format?: string;
  quality?: string;
  size?: string;
  added_at?: string;
  released?: string | null;
  isbn?: string | null;
  publisher?: string | null;
  pages?: number | null;
  narrator?: string | null;
  duration?: string | null;
  series_total?: number | null;
  files?: Array<{ kind: string; quality: string; size: string; path: string }>;
  timeline?: Array<{ t: string; e: string }>;
}

export type MockBook = BookListItem & MockBookDetail;

function sortName(name: string): string {
  const parts = name.split(/,\s*/)[0].trim().split(/\s+/);
  if (parts.length === 1) return name;
  const last = parts[parts.length - 1];
  const rest = parts.slice(0, -1).join(" ");
  return `${last}, ${rest}`;
}

function buildFiles(
  displayStatus: string | undefined,
  format: string | undefined,
  quality: string | undefined,
  size: string | undefined,
  authorName: string,
  title: string,
): MockBookDetail["files"] {
  if (
    (displayStatus === "imported" || displayStatus === "downloading") &&
    format &&
    format !== "—"
  ) {
    const authorSlug = authorName
      .split(",")[0]
      .trim()
      .toLowerCase()
      .replace(/\s+/g, "-");
    const titleSlug = title.toLowerCase().replace(/[^a-z0-9]+/g, "-");
    const qualityShort = quality?.replace(/^[^·]+·\s*/, "") ?? "";
    return [
      {
        kind: format.toUpperCase(),
        quality: qualityShort,
        size: size ?? "",
        path: `/library/${authorSlug}/${titleSlug}.${format}`,
      },
    ];
  }
  return [];
}

function buildTimeline(
  displayStatus: string | undefined,
  addedAt: string,
): MockBookDetail["timeline"] {
  const tl: Array<{ t: string; e: string }> = [];
  tl.push({ t: `${addedAt} 10:00`, e: "Added to library" });
  if (displayStatus === "wanted") {
    tl.push({ t: `${addedAt} 10:01`, e: "Marked wanted · monitoring indexers" });
  }
  if (displayStatus === "downloading") {
    tl.push({ t: `${addedAt} 10:01`, e: "Searched indexers · 4 results" });
    tl.push({ t: `${addedAt} 10:03`, e: "Snatched · qBittorrent" });
  }
  if (displayStatus === "imported") {
    tl.push({ t: `${addedAt} 10:01`, e: "Searched indexers · results found" });
    tl.push({ t: `${addedAt} 10:04`, e: "Downloaded · qBittorrent" });
    tl.push({ t: `${addedAt} 10:08`, e: "Imported to library" });
  }
  if (displayStatus === "missing") {
    tl.push({ t: `${addedAt} 10:01`, e: "No results · will retry" });
  }
  return tl;
}

